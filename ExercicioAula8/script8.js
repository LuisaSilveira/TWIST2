/*Faça um programa que receba três números inteiros, calcule e mostre a soma desses números.*/

let num1, num2, num3;
num1 = parseInt(prompt("Digite o numero inteiro um:"));
num2 = parseInt(prompt("Digite o numero inteiro dois:"));
num3 = parseInt(prompt("Digite o numero inteiro tres:"));
alert(`Média aritmética: ${num1} + ${num2} + ${num3} = ${num1+num2+num3}`);